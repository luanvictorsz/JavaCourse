package Atividades;

public class Atividade006 {
	public static void Executar() {
		int x = 100;
		
		//Raiz quadrada
		System.out.println(Math.sqrt(x));
		
		//Potenciação
		System.out.println(Math.pow(x, 2));
		
		//valor absoluto
		System.out.println(Math.abs(x));
		
		//mais informações em: java.lang.Math
	}
}
